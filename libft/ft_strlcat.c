#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	dlen;
	size_t	slen;

	dlen = ft_strlen(dst);
	slen = ft_strlen(src);
	if (dstsize < dlen)
		dlen = dstsize;
	else
		ft_strlcpy(dst + dlen, src, dstsize - dlen);
	return (dlen + slen);
}
